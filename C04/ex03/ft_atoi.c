int	ft_atoi(char *str)
{
	int	x;
	int	y;

	x = 0;
	y = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
	{
		str++;
	}
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			y *= -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		x = (x * 10) + (*str - '0');
		str++;
	}
	return (x * y);
}
